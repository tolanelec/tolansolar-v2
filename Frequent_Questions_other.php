				<p><Strong>What other equipment do I need</strong>
				<p> Usually an Inverter is required along with batteries and a controller</p>
			<p>what kind of inverter do I need? </p>
The type and size of inverter necessary depends on your application. To
determine this you must first calculate the maximum amount of load you will be
running on the inverter at one time.
There are basically four size ranges of inverters. The first is 50-300 watts; these
are small portable inverters. These inverters are ideal for laptops, small lights and
other minimal draw <abbr title="alternating current">AC</abbr> loads. </p> <p>
The next size range is 300-800 watts. Also somewhat portable and still
have the outlets on the front of the inverter. This size inverter is good for small
microwaves and other small appliances.
The third range is from 800-2000 watts. These units usually have battery chargers
built into them. Your can recharge your batteries with utility power or generator
power. They are also great for powering medium size <abbr ="alternating current">AC</abbr> loads, or running
multiple smaller loads at one time. These units are typically permanently
installed. </p> <p>
Lastly are permanently installed units ranging 2,000-11,000
watts. They function much like small generators, yet are completely silent in
operation. They are great for your larger loads, such as refrigerators.
The other question that needs to be answered when selecting an inverter is the
type of wave form. If you are running sensitive electronic equipment, like fax
machines, laser printers or high tech stereo equipment you need a sine wave
inverter. A sine wave inverter has a wave form that is very similar to the form of
grid electricity. Other options may include modified sine wave, which is fine for
items that are not supersensitive to clean power. You may experience a humming
sound when powering clocks, and small radios on a modified sine wave inverter.
				.</p>
<p><strong>Batteries: What Kind of Battery do I need?</strong></p>
  <p>
The most common type of battery used in a solar system is a lead-acid battery.
They are generally used because they have a low initial cost and are readily
available. These batteries must be deep-cycle batteries. If the battery is a shallow
cycle or automotive type it will not function correctly in the system. The deep
cycle batteries are designed to discharge and recharge or cycle day after day for
years. </p><p>
So you need to decide which battery fits your specific needs. As long as the
battery is taken care of you can expect a long battery life using either battery type.
When do I need a charge controller and why?
</p><p><strong>
charge controller/regulator</strong>
 Controllers or regulators are used ensure that batteries are correctly charged to capacity without overcharging.
if a solar array is connected to a battery with no overcharge protection, it could damage the batteries. There
are different ranges of controllers from simple contollers with a single relay, to sophisticated controllers 
with several stages and charging sequences to assure battery is fully charged.</p>
