			<span id="contents3">
			
				<p><strong>What applications can photovoltaic electricity and technology be put to ?</strong></p>
				<p><Strong>Solar powered lights </strong>
				Solar lights that charge during the day and light up at dusk are now commonly used.
				Solar-charged lanterns have become popular in developing countries where they provide a safer 
				and cheaper alternative to kerosene lamps. 
				<br /> Solar powered energy saving bulbs offer superior light output to kerosine or candle light due to inefficient combustion of fuel.
				Compared to energy saving bulbs (<abbr>CFL</abbr>), the light from kerosine lanterns is not a natural color that is adequate for reading or general lighting needs.
			<p><strong>Thermal technologies</strong>
			Solar thermal technologies can be used for water heating, space heating, 
				space cooling and process heat generation
				<br />
				Solar hot water systems use sunlight to heat water. In low geographical latitudes 
				(below 40 degrees) from 60 to 70% of the domestic hot water use with temperatures up to 60 °C 
				can be provided by solar heating systems. 	The most common types of solar water heaters are evacuated tube collectors (44%)
				and glazed flat plate collectors (34%) generally used for domestic hot water; 
				and unglazed plastic collectors (21%) used mainly to heat swimming pools.
				<br />
				Thermal mass is any material that can be used to store heat—heat from the Sun in the case of solar energy. 
				Common thermal mass materials include stone, cement and water. thermal mass maintains space temperatures in a comfortable range and reduces
				the need for auxiliary heating and cooling equipment.
				</P>
				
				<p><Strong>Solar electricity </strong>
				Solar power is the conversion of sunlight into electricity. Through technology called
Photovoltaics (<abbr title="photo voltaic ">PV</abbr>) electrical power can be generated by converting solar radiation into direct current electricity 
using semiconductors that have photovoltaic properties. <abbr title="photo voltaic ">PV</abbr> power generation uses solar panels composed of a number of solar cells containing 
a photovoltaic material. Solar cells don't always need direct sunlight to work - they can still generate some electricity on a cloudy day. 
The cells convert the sunlight into electricity, which can be used to run household appliances and lighting.
<br />
 Photovoltaic arrays in buildings are often either integrated into them, or mounted on the roof tops. 
 Arrays are most often retrofitted into existing buildings, usually mounted on top of the existing roof structure.
 <br />
 How it works- Daylight hits the Photovoltaic (<abbr title="photo voltaic ">PV</abbr>) cells and is converted to clean electricity.
 An inverter converts solar electricity from direct current to alternating current for use in the house.
 Electricity can be stored in batteries which can be later used during the night to charge household appliances and 
 lighting.
</p>
