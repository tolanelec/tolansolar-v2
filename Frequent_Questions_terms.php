                 current </p><p><Strong>Monocrystalline</strong>:Most efficient of the solar
                                  cells. </p> <p><strong>Polycrystallaline</strong>: second most efficient.</p><p>
                           <strong>Amorphous </strong>:Least efficient but considerably 
