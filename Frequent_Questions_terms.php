<p><strong>  <abbr title="photo voltaic ">PV</abbr> Modules </strong>- one solar cell produces 0.5 volts and 36 cells are connected
together to charge 12 volt batteries, pumps and motors. The new standard is 72
cells per module for 24 volts and operating at about 30 volts. The module is the
basic building block of systems. The <abbr title="photo voltaic ">PV</abbr> cells are connected in series and/or
parallel and encapsulated in an environmentally protective laminate</p> <p> <strong>Integrated <abbr title="photo voltaic ">PV</abbr> Modules </strong>
are often constructed to look like materials normally found in construction – i.e., shingles <abbr title="photo voltaic ">PV</abbr> Array – A group of panels that comprises the complete direct current PV
generating unit. (Sometimes referred to as a “string”).</p>
<p> <strong>
Pre-tabbed cells</strong>
: PV-solar cells with tab ribbon 
