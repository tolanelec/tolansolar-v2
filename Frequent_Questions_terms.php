<p><strong>Water heating</strong>Two types of solar water heating systems are
Direct or open loop systems, in which potable water is heated directly in collector 
Indirect or closed loop system, in which potable water is heated indirectly by a 
heat transfer fluid that is heated in the collector and passes through a heat exchanger
to tranfer heat to domestic or service water. </P>
